n = input('Digite algo: ')
print(f'O tipo primitivo desse valor é: {type(n)}\033[m.')
print(f'Só tem espaços? \033[32m{n.isspace()}\033[m.')
print(f'É numérico? \033[32m{n.isnumeric()}\033[m.')
print(f'É alfabético? \033[32m{n.isalpha()}\033[m.')
print(f'É alfanumérico? \033[32m{n.isalnum()}\033[m.')
print(f'Está em maiúsculo? \033[32m{n.isupper()}\033[m.')
print(f'Está em minúsculo? \033[32m{n.islower()}\033[m.')
print(f'Está capitalizada? \033[32m{n.istitle()}\033[m.')
